    public void fillDaTa(int index) {
        ChucVuViewModel chucVu = listChucVuView.get(index);
        txtID.setText(chucVu.getId());
        txtMa.setText(chucVu.getMa());
        txtTen.setText(chucVu.getTen());
    }

